// Javascript : Conditional Statements

// Even or Odd
let Number = 5;

if (Number % 2 == 0) {
    console.log("Number is Even");
} else {
    console.log("Number is Odd")
}

// Days of the week
var day = 4;

switch (day) {
    case 1:
        day = "Monday";
        break;
    case 2:
        day = "Tuesday";
        break;
    case 3:
        day = "Wednsday";
        break;
    case 4:
        day = "Thursday";
        break;
    case 5:
        day = "Friday";
        break;
    case 6:
        day = "Saturday";
        break;
    case 7:
        day = "Sunday";
        break;
    default:
        day = "Unvalid Day";
        break;
}

