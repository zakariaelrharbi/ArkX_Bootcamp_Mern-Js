// Function that Add two numbers and return the result.
//    (x, y) => (x + y);

function addNum(x, y) {
    return x + y;
}   
// Function that Subtract two numbers and return the result.
//    (x, y) => (x - y);
   
function SubtractNum(x, y) {
    return x - y;
} 
// Function that Multiply two numbers and return the result.
//    (x, y) => (x * y);
      
function MultiplyNum(x, y) {
    return x * y;
}       
// Function that Divide two numbers and return the result.
//     (x, y) => (x / y);
function DivideNum(x, y) {
    return x / y;
}
console.log(addNum(5, 9));
console.log(SubtractNum(2, 4));
console.log(MultiplyNum(2, 3));
console.log(DivideNum(2, 0));
